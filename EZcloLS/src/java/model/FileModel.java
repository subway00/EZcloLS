package model;

public class FileModel {
    private int number;
    private String name;

    public FileModel(int number, String name) {
        this.number = number;
        this.name = name;
    }

    public int getNumber() {
        return number;
    }

    public String getName() {
        return name;
    }

    
    
    
}
